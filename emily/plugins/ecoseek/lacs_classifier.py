def train_lacs_model(
    domain: str,
    positive_abstracts: list[dict],
    random_sample_size: int = 5000,
) -> dict:
    """Train a new LACS model on the cluster for a custom domain.

    Parameters
    ----------
    domain : str
        Name for the new domain model (e.g., "coral-bleaching").
    positive_abstracts : list[dict]
        List of known-positive papers, each with "doi", "title", "abstract".
    random_sample_size : int
        Number of random abstracts to use as unlabeled class.

    Returns
    -------
    dict
        Training result with model path, metrics, and status.
    """
    if not _API_KEY:
        return {"error": "Hermes API key required for model training", "success": False}

    if len(positive_abstracts) < 10:
        return {"error": "Need at least 10 positive abstracts to train a model", "success": False}

    model_path = f"{_MODELS_DIR}/{domain}.rds"

    # Build R training script
    abstracts_json = json.dumps(positive_abstracts, ensure_ascii=False)
    r_code = f"""
suppressMessages({{
  library(lacs)
  library(jsonlite)
}})

# Load positive abstracts
positive_papers <- fromJSON('{abstracts_json}')

# Create abstracts data frame with positive class
df_positive <- data.frame(
  doi = positive_papers$doi,
  title = positive_papers$title,
  abstract = positive_papers$abstract,
  class = "positive",
  stringsAsFactors = FALSE
)

# Generate random unlabeled abstracts from PubMed FTS5 index
# (or use built-in random sample if index not available)
pubmed_db <- "/home/a474r867/work/pubmed/index/pubmed_fts.db"
if (file.exists(pubmed_db)) {{
  con <- DBI::dbConnect(RSQLite::SQLite(), pubmed_db)
  random_abstracts <- DBI::dbGetQuery(con,
    "SELECT '' AS doi, title, abstract FROM pubmed_papers ORDER BY RANDOM() LIMIT {random_sample_size}")
  DBI::dbDisconnect(con)
}} else {{
  # Fallback: use lacs built-in sample data
  random_abstracts <- lacs::hp3_abstracts[sample(nrow(lacs::hp3_abstracts), min({random_sample_size}, nrow(lacs::hp3_abstracts))), ]
}}

df_unknown <- data.frame(
  doi = random_abstracts$doi,
  title = random_abstracts$title,
  abstract = random_abstracts$abstract,
  class = "unknown",
  stringsAsFactors = FALSE
)

# Combine and train
df_all <- rbind(df_positive, df_unknown)
abstracts_obj <- get_abstracts(df_all)

# Build vocabulary
vocab <- get_vocabulary(abstracts_obj,
  term_count_min = 5,
  doc_proportion_min = 0.01,
  doc_proportion_max = 0.5)

# Train LACS model
model <- lacs(abstracts_obj,
  vocabulary = vocab,
  alpha = 1,
  sample_use_time = 200,
  learning_rate = 1,
  qq = 0.001,
  tf_idf = TRUE)

# Save model
dir.create("{_MODELS_DIR}", recursive = TRUE, showWarnings = FALSE)
saveRDS(model, "{model_path}")

# Quick self-assessment
newX <- get_dtm(abstracts_obj, vocab, tf_idf = TRUE)
pred <- predict(model$plus$fit.pi, newx = newX, s = "lambda.min", type = "response")
y_true <- ifelse(df_all$class == "positive", 1, 0)
auc_val <- tryCatch({{
  pROC::auc(y_true, as.numeric(pred))
}}, error = function(e) NA)

result <- list(
  success = TRUE,
  model_path = "{model_path}",
  domain = "{domain}",
  n_positive = nrow(df_positive),
  n_unlabeled = nrow(df_unknown),
  vocab_size = nrow(vocab),
  auc = as.numeric(auc_val)
)
cat(toJSON(result, auto_unbox = TRUE))
"""

    payload = {
        "model": "hermes-agent",
        "messages": [
            {
                "role": "system",
                "content": (
                    "You are executing an R training script on the HPC cluster. "
                    "Run the R code using the geospatial SIF and return the JSON output."
                ),
            },
            {
                "role": "user",
                "content": (
                    f"Execute this R code on the cluster using:\n"
                    f"apptainer exec --bind ~/R/library:/usr/local/lib/R/site-library "
                    f"{_SIF_PATH} Rscript -e '<code>'\n\n"
                    f"R code:\n```r\n{r_code}\n```\n\n"
                    f"Return only the JSON output."
                ),
            },
        ],
    }

    try:
        from .http_client import http_post_json
        result = http_post_json(
            f"{_HERMES_URL}/v1/chat/completions",
            payload,
            headers={"Authorization": f"Bearer {_API_KEY}"},
            timeout=120,
        )

        content = result.get("choices", [{}])[0].get("message", {}).get("content", "")
        parsed = _extract_json(content)
        if parsed:
            return parsed
        return {"success": False, "error": "Could not parse training result", "raw": content[:500]}

    except Exception as exc:
        return {"success": False, "error": str(exc)[:200]}
