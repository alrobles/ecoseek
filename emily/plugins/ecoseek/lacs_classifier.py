def _score_local(abstract: str, domain: str) -> float:
    """Lightweight keyword-frequency heuristic (0.0-1.0).

    Used as fallback when the cluster is unreachable.
    """
    if not abstract or len(abstract) < 20:
        return 0.0

    keywords = _DOMAIN_KEYWORDS.get(domain, _DOMAIN_KEYWORDS["host-parasite"])
    text_lower = abstract.lower()
    hits = sum(1 for kw in keywords if kw in text_lower)
    return min(hits / max(len(keywords) * 0.4, 1), 1.0)


# ---------------------------------------------------------------------------
# Remote scoring via Hermes → R execution
# ---------------------------------------------------------------------------

def _build_r_scoring_script(abstracts: list[str], domain: str) -> str:
    """Generate R code that loads a LACS model and scores abstracts."""
    model_path = f"{_MODELS_DIR}/{domain}.rds"

    abstracts_json = json.dumps(abstracts, ensure_ascii=False)

    return f"""
# LACS scoring script — auto-generated by Emily
suppressMessages({{
  library(lacs)
  library(jsonlite)
}})

model_path <- "{model_path}"
if (!file.exists(model_path)) {{
  cat(toJSON(list(error = paste("Model not found:", model_path)), auto_unbox = TRUE))
  quit(status = 0)
}}

lacs_model <- readRDS(model_path)

abstracts <- fromJSON('{abstracts_json}')

scores <- tryCatch({{
  predictions <- predict(lacs_model, abstracts)
  as.numeric(predictions)
}}, error = function(e) {{
  # Fallback: score each abstract individually
  sapply(abstracts, function(a) {{
    tryCatch({{
      newX <- get_dtm(
        get_abstracts(data.frame(
          doi = "temp", title = "temp",
          abstract = a, class = "unknown",
          stringsAsFactors = FALSE
        )),
        lacs_model$vocabulary,
        tf_idf = TRUE
      )
      pred <- predict(lacs_model$plus$fit.pi,
                      newx = newX,
                      s = "lambda.min",
                      type = "response")
      as.numeric(pred[1, 1])
    }}, error = function(e2) 0.0)
  }})
}})

result <- list(
  scores = scores,
  domain = "{domain}",
  model_path = model_path,
  n_scored = length(scores)
)
cat(toJSON(result, auto_unbox = TRUE))
"""


def score_abstracts_remote(
    abstracts: list[str],
    domain: str = _DEFAULT_DOMAIN,
) -> list[float]:
    """Score abstracts using the cluster LACS model via Hermes.

    Returns a list of scores (0.0-1.0) for each abstract.
    """
    if not _API_KEY:
        logger.warning("No Hermes API key — falling back to local scoring")
        return [_score_local(a, domain) for a in abstracts]

    r_code = _build_r_scoring_script(abstracts, domain)

    payload = {
        "model": "hermes-agent",
        "messages": [
            {
                "role": "system",
                "content": (
                    "You are executing an R script on the HPC cluster. "
                    "Run the following R code using the geospatial SIF container "
                    "and return ONLY the JSON output. Do not add commentary."
                ),
            },
            {
                "role": "user",
                "content": (
                    f"Execute this R code on the cluster using:\n"
                    f"apptainer exec --bind ~/R/library:/usr/local/lib/R/site-library "
                    f"{_SIF_PATH} Rscript -e '<code>'\n\n"
                    f"R code:\n```r\n{r_code}\n```\n\n"
                    f"Return only the JSON output from the script."
                ),
            },
        ],
    }

    try:
        from .http_client import http_post_json
        result = http_post_json(
            f"{_HERMES_URL}/v1/chat/completions",
            payload,
            headers={"Authorization": f"Bearer {_API_KEY}"},
            timeout=_TIMEOUT,
        )

        content = result.get("choices", [{}])[0].get("message", {}).get("content", "")

        # Try to extract JSON from the response
        try:
            # Look for JSON in the response
            json_match = _extract_json(content)
            if json_match and "scores" in json_match:
                scores = json_match["scores"]
                if isinstance(scores, list) and len(scores) == len(abstracts):
                    return [float(s) for s in scores]
        except (json.JSONDecodeError, ValueError, TypeError):
            pass

        logger.warning("Could not parse LACS scores from Hermes response; using local fallback")
        return [_score_local(a, domain) for a in abstracts]

    except Exception as exc:
        logger.warning("Hermes LACS scoring failed: %s; using local fallback", exc)
        return [_score_local(a, domain) for a in abstracts]


def _extract_json(text: str) -> dict | None:
    """Extract a JSON object from mixed text (LLM response may have wrapping)."""
    import re
    # Try the full text first
    try:
        return json.loads(text)
    except (json.JSONDecodeError, ValueError):
        pass

    # Try to find JSON in code blocks
    code_block = re.search(r"```(?:json)?\s*\n?(.*?)\n?```", text, re.DOTALL)
    if code_block:
        try:
            return json.loads(code_block.group(1))
        except (json.JSONDecodeError, ValueError):
            pass

    # Try to find a JSON object anywhere
    brace = text.find("{")
    if brace >= 0:
        depth = 0
        for i, ch in enumerate(text[brace:], start=brace):
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
            if depth == 0:
                try:
                    return json.loads(text[brace : i + 1])
                except (json.JSONDecodeError, ValueError):
                    break
    return None


