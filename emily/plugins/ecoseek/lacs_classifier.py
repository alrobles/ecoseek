def classify_abstracts(
    abstracts: list[str],
    domain: str = _DEFAULT_DOMAIN,
    use_cluster: bool = True,
) -> list[dict]:
    """Classify a list of abstracts by domain relevance.

    Parameters
    ----------
    abstracts : list[str]
        Text of each abstract to score.
    domain : str
        Pre-trained domain model to use (e.g., "host-parasite").
    use_cluster : bool
        If True, try cluster first; fallback to local heuristic.

    Returns
    -------
    list[dict]
        For each abstract: {"score": float, "relevant": bool, "method": str}
    """
    if not abstracts:
        return []

    if use_cluster and _LACS_ENABLED and _API_KEY:
        scores = score_abstracts_remote(abstracts, domain)
        method = "lacs_cluster"
    else:
        scores = [_score_local(a, domain) for a in abstracts]
        method = "keyword_heuristic"

    return [
        {
            "score": round(s, 4),
            "relevant": s >= _LACS_THRESHOLD,
            "method": method,
            "domain": domain,
        }
        for s in scores
    ]


def rerank_evidence(
    evidence_list: list[dict],
    domain: str = _DEFAULT_DOMAIN,
) -> list[dict]:
    """Re-rank retrieved evidence by LACS domain relevance.

    Modifies evidence dicts in-place: adds ``lacs_score`` and
    ``lacs_relevant`` fields, then re-sorts by combined score.

    Parameters
    ----------
    evidence_list : list[dict]
        Evidence dicts from retrieval (must have "abstract" key).
    domain : str
        LACS domain model.

    Returns
    -------
    list[dict]
        Same list, sorted by combined relevance score.
    """
    if not evidence_list:
        return evidence_list

    abstracts = [
        e.get("abstract", "") or e.get("title", "")
        for e in evidence_list
    ]

    classifications = classify_abstracts(abstracts, domain=domain)

    for ev, cls in zip(evidence_list, classifications):
        ev["lacs_score"] = cls["score"]
        ev["lacs_relevant"] = cls["relevant"]
        ev["lacs_method"] = cls["method"]
        ev["lacs_domain"] = cls["domain"]
        # Combined score: original confidence weighted by LACS relevance
        original_confidence = ev.get("confidence", 0.5)
        ev["combined_score"] = round(
            0.4 * original_confidence + 0.6 * cls["score"], 4
        )

    # Re-sort by combined score (highest first)
    evidence_list.sort(key=lambda e: e.get("combined_score", 0), reverse=True)

    return evidence_list


