"""LACS — Literature Abstract Classification System integration.

Provides domain-specific relevance scoring for scientific literature using
Positive-Unlabeled (PU) learning via the LACS/xplus R packages on the HPC
cluster.  Two execution modes:

  1. **Remote (cluster)** — sends abstracts to Hermes → R execution in the
     geospatial SIF.  Requires a pre-trained ``.rds`` model on the cluster.
  2. **Local (lightweight)** — uses a keyword-frequency heuristic when the
     cluster is unreachable.  Fast but less accurate.

Design follows alrobles/lacs (validate → vocabulary → DTM → PLUS) and the
dissertation Chapter 2 (LACS: A Lightweight Open-Source System for
Domain-Specific Scientific Literature Classification).

Cluster paths:
  - Models:     /home/a474r867/work/lacs_models/<domain>.rds
  - SIF:        /home/a474r867/geospatial-rserver/geospatial_latest.sif
  - R library:  ~/R/library

Env vars:
  LACS_ENABLED         — enable LACS scoring (default: true)
  LACS_DEFAULT_DOMAIN  — default model domain (default: host-parasite)
  LACS_THRESHOLD       — minimum score to consider relevant (default: 0.5)
"""
